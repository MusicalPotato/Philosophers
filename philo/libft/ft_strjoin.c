#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		x;

	if (!s1 || !s2)
		return (0);
	str = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!str)
		return (0);
	x = 0;
	while (*s1)
		str[x++] = *s1++;
	while (*s2)
		str[x++] = *s2++;
	str[x] = '\0';
	return (str);
}
