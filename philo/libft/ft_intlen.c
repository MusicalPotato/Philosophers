int	ft_intlen(long nbr)
{
	int	x;

	x = 0;
	if (nbr >= 10)
		x = ft_intlen(nbr / 10);
	else if (nbr < 0)
		x = ft_intlen(nbr * -1);
	return (x + 1);
}
