#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	x;

	x = 0;
	if (!s)
		return (0);
	while (s[x] != (char)c && s[x])
		x++;
	if (s[x] == (char)c)
		return ((char *)s + x);
	return (0);
}
